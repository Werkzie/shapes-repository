
public class Sphere extends Shape{
	// Variables
	private double radius = 0.0;
	
	// Constructor
	public Sphere(float radius) {
        this.radius = radius;
    }

	// Getter and Setter
	public double getRadius() {
		return radius;
	}

	public void setRadius(double radius) {
		this.radius = radius;
	}
	
}
